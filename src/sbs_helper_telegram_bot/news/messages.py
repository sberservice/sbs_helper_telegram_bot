# ===== –®–ê–ë–õ–û–ù –ù–û–í–û–°–¢–ù–û–ô –°–¢–ê–¢–¨–ò =====

def format_news_article(
    title: str,
    content: str,
    category_emoji: str,
    category_name: str,
    published_date: str,
    reactions: dict = None
) -> str:
    """
    –°—Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞—Ç—å –Ω–æ–≤–æ—Å—Ç–Ω—É—é —Å—Ç–∞—Ç—å—é –¥–ª—è –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è.
    
    Args:
        title: –∑–∞–≥–æ–ª–æ–≤–æ–∫ —Å—Ç–∞—Ç—å–∏ (—É–∂–µ —ç–∫—Ä–∞–Ω–∏—Ä–æ–≤–∞–Ω).
        content: —Å–æ–¥–µ—Ä–∂–∞–Ω–∏–µ —Å—Ç–∞—Ç—å–∏ (—É–∂–µ –≤ MarkdownV2).
        category_emoji: —ç–º–æ–¥–∑–∏ –∫–∞—Ç–µ–≥–æ—Ä–∏–∏.
        category_name: –Ω–∞–∑–≤–∞–Ω–∏–µ –∫–∞—Ç–µ–≥–æ—Ä–∏–∏ (—É–∂–µ —ç–∫—Ä–∞–Ω–∏—Ä–æ–≤–∞–Ω–æ).
        published_date: –¥–∞—Ç–∞ –ø—É–±–ª–∏–∫–∞—Ü–∏–∏ (—É–∂–µ —ç–∫—Ä–∞–Ω–∏—Ä–æ–≤–∞–Ω–∞).
        reactions: —Å–ª–æ–≤–∞—Ä—å —Å –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ–º —Ä–µ–∞–∫—Ü–∏–π like/love/dislike.
        
    Returns:
        –û—Ç—Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–Ω—ã–π —Ç–µ–∫—Å—Ç —Å—Ç–∞—Ç—å–∏.
    """
    article = f"{category_emoji} *{title}*\n"
    article += f"_{category_name} ‚Ä¢ {published_date}_\n\n"
    article += content
    
    if reactions:
        article += "\n\n"
        reaction_parts = []
        if reactions.get('like', 0) > 0:
            reaction_parts.append(f"üëç {reactions['like']}")
        if reactions.get('love', 0) > 0:
            reaction_parts.append(f"‚ù§Ô∏è {reactions['love']}")
        if reactions.get('dislike', 0) > 0:
            reaction_parts.append(f"üëé {reactions['dislike']}")
        if reaction_parts:
            article += " ‚Ä¢ ".join(reaction_parts)
    
    return article


